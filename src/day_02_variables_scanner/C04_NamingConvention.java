package day_02_variables_scanner;

public class C04_NamingConvention {
    public static void main(String[] args) {

        String text = "Java is fun to learn";
        String teXT = "Java is easy to learn";
        System.out.println(text);
        System.out.println(teXT);
        // variables must start with a letter, _ or $ sign, not with a number. Lower/Upper case doesn't matter.

    }
}
